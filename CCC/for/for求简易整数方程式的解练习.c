#include <stdio.h>
int main() {
	int i, j;
	for (i = 1; i <= 30/2; ++i)
	{//如果输出时i是较小的那个数字,那么i最多找到15就可以了.
		for (j = 30/2; j <= 30; ++j)
		{//同上,如果j是较大的数字,就可以从15向上找.			因为这两个数字不能大于十五,不然相加会超过三十,也不可能都小于十五,那就不可能等于三十.
			//printf("%d,%d\n", i, j);
			if (i + j == 30 && i * j == 221 && i <= j)
			{
				printf("%d,%d\n", i, j);
			}
		}
	}
	return 0;
}